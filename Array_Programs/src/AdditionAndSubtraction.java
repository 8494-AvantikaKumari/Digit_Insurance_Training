import java.util.*;
public class AdditionAndSubtraction {
    public static void main(String[] args){
        Scanner scn=new Scanner(System.in);
        int a=scn.nextInt();
        int b=scn.nextInt();
        int sum=a+b;
        System.out.println(sum);
        int diff=a-b;
        System.out.println(diff);
    }
}
